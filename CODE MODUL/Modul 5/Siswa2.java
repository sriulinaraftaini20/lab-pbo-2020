//Kelas Siswa Menggunakan Keyword This

public class Siswa2 {
	private int nrp;

	public Siswa2(int nrp){
		nrp = nrp;
	}
	public void setNrp(int nrp) {
		this.nrp = nrp;
	}
	public int getNrp() {
		return nrp;
	}
}


